#Type Checking

print(type("adi")) # O/P : <class 'str'>
print(type("123"))  # O/P : <class 'str'>
print(type(123))  # O/P : <class 'int'>
print(type(123_456_789))  # O/P : <class 'int'>
print(type(123456789))  # O/P : <class 'int'>
print(type(123.45))  # O/P : <class 'float'>
print(type("True"))  # O/P : <class 'str'>
print(type(True))  # O/P : <class 'bool'>
print(type(False))  # O/P : <class 'bool'>
