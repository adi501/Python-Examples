#List

list_of_numbers = [1,2,3,5]
list_of_numbers[1] = 10  # update Index 1
list_of_numbers.append(60)  # adding new number
list_of_numbers.extend([11, 12,13]) # Extending 3 numbers
print(list_of_numbers[2]) # it will print Index 2 number
print(list_of_numbers[-1]) # it will print last index number
print(list_of_numbers[-2]) # it will print 2nd last index number
print(list_of_numbers)
